function applyRules(text, rules) {
  let out = text;
  const sorted = [...rules].sort((a, b) => b[0].length - a[0].length);
  for (const [from, to] of sorted) {
    out = out.split(from).join(to);
  }
  return out;
}

function writePatch(locale, rules) {
  const out = {};
  for (const [key, enVal] of Object.entries(wiring3)) {
    out[key] = applyRules(enVal, rules);
  }
  const constName = locale === "ro" ? "wiring3Ro" : `wiring3${locale.toUpperCase()}`;
  const lines = [
    `/** Generated wiring-3 ${locale} patch — review with native speaker. */`,
    `export default {`,
  ];
  for (const [k, v] of Object.entries(out).sort(([a], [b]) => a.localeCompare(b))) {
    const esc = String(v)
      .replace(/\\/g, "\\\\")
      .replace(/"/g, '\\"')
      .replace(/\n/g, "\\n");
    lines.push(`  "${k}": "${esc}",`);
  }
  lines.push("};", "");
  fs.writeFileSync(path.join(patchDir, `wiring-3-${locale}.mjs`), lines.join("\n"), "utf8");
}

for (const loc of ["ro", "pl", "es", "ru"]) {
  writePatch(loc, LOCALE_RULES[loc]);
}
